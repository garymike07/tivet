// Helper functions for trainer data
export const getTrainerById = (id) => {
    return trainersData.find(trainer => trainer.id === id);
};

export const getTrainersBySpecialty = (specialty) => {
    return trainersData.filter(trainer => 
        trainer.specialty.toLowerCase().includes(specialty.toLowerCase())
    );
};

export const getFeaturedTrainers = () => {
    return trainersData.filter(trainer => trainer.featured);
};

export const getAvailableTrainers = () => {
    return trainersData.filter(trainer => trainer.availability === 'Available');
};

export const searchTrainers = (query) => {
    const lowercaseQuery = query.toLowerCase();
    return trainersData.filter(trainer => 
        trainer.name.toLowerCase().includes(lowercaseQuery) ||
        trainer.specialty.toLowerCase().includes(lowercaseQuery) ||
        trainer.bio.toLowerCase().includes(lowercaseQuery) ||
        trainer.skills.some(skill => skill.toLowerCase().includes(lowercaseQuery))
    );
};

export const getTrainersByRating = (minRating = 4.0) => {
    return trainersData.filter(trainer => trainer.rating >= minRating);
};

export const getTrainersByExperience = (minYears = 10) => {
    return trainersData.filter(trainer => trainer.experience >= minYears);
};

export const getTrainersByLocation = (location) => {
    return trainersData.filter(trainer => 
        trainer.location.toLowerCase().includes(location.toLowerCase())
    );
};

export const getTrainerSpecialties = () => {
    const specialties = [...new Set(trainersData.map(trainer => trainer.specialty))];
    return specialties.map(specialty => ({
        value: specialty.toLowerCase().replace(/\s+/g, '-'),
        label: specialty,
        count: trainersData.filter(trainer => trainer.specialty === specialty).length
    }));
};

export const getTrainerStats = () => {
    const totalTrainers = trainersData.length;
    const totalStudents = trainersData.reduce((sum, trainer) => sum + trainer.students, 0);
    const totalCourses = trainersData.reduce((sum, trainer) => sum + trainer.courses, 0);
    const averageRating = trainersData.reduce((sum, trainer) => sum + trainer.rating, 0) / totalTrainers;
    
    return {
        totalTrainers,
        totalStudents,
        totalCourses,
        averageRating: Math.round(averageRating * 10) / 10
    };
};

